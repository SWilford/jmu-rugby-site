alter table public.contact_cards enable row level security;

drop policy if exists "Public read active contact cards" on public.contact_cards;
create policy "Public read active contact cards"
on public.contact_cards
for select
to anon, authenticated
using (is_active = true or public.is_admin());

drop policy if exists "Admins insert contact cards" on public.contact_cards;
create policy "Admins insert contact cards"
on public.contact_cards
for insert
to authenticated
with check (public.is_admin());

drop policy if exists "Admins update contact cards" on public.contact_cards;
create policy "Admins update contact cards"
on public.contact_cards
for update
to authenticated
using (public.is_admin())
with check (public.is_admin());

drop policy if exists "Admins delete contact cards" on public.contact_cards;
create policy "Admins delete contact cards"
on public.contact_cards
for delete
to authenticated
using (public.is_admin());
